<h4>Project Suggestions</h4>
<b>Yelp Dataset Challenge</b>
<p>Predict ratings, user behavior from the Yelp dataset. More information is given on the <a href="http://www.yelp.com/dataset_challenge/">Yelp Dataset Challenge</a>.</p>
<br />
<b>Airfare prediction</b>
<p>Look at the current air fare for various flights and build a system that predicts the best time to buy a ticket.</p>
<br />
<b>Handwriting Recognition</b>
<p>Use Hidden Markov Models for designing a handwriting recognition system or something as cool as <a href="http://detexify.kirelabs.org/classify.html">Detexify</a>.</p>
<br />
<b>Moneyball</b>
<p>Use player statistics to predict player performance. Then see how well your prediction works by testing it on a tournament dataset. Basically make an algorithm that can play fantasy baseball, football or soccer. See <a href="http://www.soccermetrics.net/">Soccermetrics</a>.</p>
<br />
<b>Sound denoising or Shazzam</b>
<p>Projects for doing audio processing. You can use unsupervised learning techniques to remove noise from an audio signal. You can work on a semi-supervised learning algorithm for performing speech recognition or developing an application like Shazzam.</p>
<br />
<b>Quantified Self and Data Analysis</b>
<p>Use any of the many available quantified self devices, record dataset and try to make sense out of it. You will find many such devices to gather the data you are interested in. You can use your phone to track your runs, bike rides and devices like fitbit, ecg monitors and so on. I am especially interested in this project because of this <a href="http://www.qualcommtricorderxprize.org/">competition</a>.</p>
